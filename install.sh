  echo -e '['$cyan_fg'*'$reset']'$cyan_fg" Cloning ohmyzsh to $HOME/.haxtools/home/.oh-my-zsh\n"$reset
  git clone https://github.com/ohmyzsh/ohmyzsh $HOME/.haxtools/home/.oh-my-zsh
else
  echo -e '['$cyan_fg'*'$reset']'$cyan_fg" Pulling latest ohmyzsh\n"$reset
  git -C $HOME/.haxtools/home/.oh-my-zsh pull
