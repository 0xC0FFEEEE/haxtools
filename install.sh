# SSH keys
if [[ ! -d $HOME/.haxtools/home/.ssh ]]; then
  mkdir $HOME/.haxtools/home/.ssh
fi
if grep -Eq 'gpgconf.*agent-ssh-socket' $shellrc && ! grep -Eq 'gpgconf.*agent-ssh-socket' $HOME/.haxtools/home/.zshrc && [[ ! -s $HOME/haxtools/home/.ssh/id_rsa ]]; then
  echo -e '['$cyan_fg$blink'*'$reset']'$cyan_fg' You appear to be using gpg ssh auth, would you like to use it in haxtools? '$reset'[Y/n]\n'
  until [[ reply =~ ^(y|Y|''|n|N)$ ]]; do
    read -e -p ">> " reply
    if [[ $reply =~ ^(n|N)$ ]]; then
      echo -e '\n['$cyan_fg'*'$reset']'$cyan_fg' Skipping...\n'$reset
      break
    elif [[ $reply =~ ^(y|Y|'')$ ]]; then
      echo -e '\n['$cyan_fg'*'$reset']'$cyan_fg" Adding gpg config to $HOME/.haxtools/home/.zshrc\n"$reset
      echo -e 'export GPG_TTY="$(tty)"\nexport SSH_AUTH_SOCK=$(gpgconf --list-dirs agent-ssh-socket)\ngpgconf --launch gpg-agent' >> $HOME/.haxtools/home/.zshrc
      echo -e '['$green_fg'✓'$reset']'$cyan_fg" Done!\n"$reset
      gpgssh=true
      break
    else
      echo -e '\n['$red_fg$blink'!'$reset']'$red_fg' Try again\n'$reset
    fi
  done
fi
if [[ ! $gpgssh = true ]]; then
  if [[ -s $HOME/.ssh/id_rsa ]] && [[ ! -s $HOME/haxtools/home/.ssh/id_rsa ]] && ! grep -Eq 'gpgconf.*agent-ssh-socket' $HOME/.haxtools/home/.zshrc; then
    echo -e '['$cyan_fg$blink'*'$reset']'$cyan_fg' You have an existing ssh keypair, would you like to use it in haxtools? '$reset'[Y/n]\n'
    until [[ reply =~ ^(y|Y|''|n|N)$ ]]; do
      read -e -p ">> " reply
      if [[ $reply =~ ^(n|N)$ ]]; then
        echo -e '\n['$cyan_fg'*'$reset']'$cyan_fg' Generating ssh keypair...\n'$reset
        ssh-keygen -t rsa -b 4096 -f $HOME/.haxtools/home/.ssh/id_rsa -P ""
        echo -e '\n['$green_fg'✓'$reset']'$cyan_fg" Done!\n"$reset
        break
      elif [[ $reply =~ ^(y|Y|'')$ ]]; then
        echo -e '\n['$cyan_fg'*'$reset']'$cyan_fg" Copying existing keypair to $HOME/.haxtools/home/.ssh/\n"$reset
        cp $HOME/.ssh/id_rsa $HOME/.haxtools/home/.ssh
        cp $HOME/.ssh/id_rsa.pub $HOME/.haxtools/home/.ssh
        echo -e '['$green_fg'✓'$reset']'$cyan_fg" Done!\n"$reset
        break
      else
        echo -e '\n['$red_fg$blink'!'$reset']'$red_fg' Try again\n'$reset
      fi
    done
  fi
fi

