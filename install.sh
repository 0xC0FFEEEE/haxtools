if [[ $shell = '/bin/zsh' ]]; then
  shellrc='.zshrc'
  echo -e '['$cyan_fg'*'$reset']'$cyan_fg' You'"'"'re using zsh, nice!\n'$reset
  if ! grep -Fxq '  source $HOME/.haxtools/.haxrc' $HOME/.zshrc; then
